#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int	i;

	i = 0;
	if (c > 255)
		c -= 256;
	if (!ft_isascii(c))
		return (NULL);
	while (s[i])
	{
		i++;
	}
	while (i >= 0)
	{
		if (s[i] == c)
			return ((char *)&s[i]);
		i--;
	}
	return (NULL);
}
